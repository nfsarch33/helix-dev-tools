func TestGenerateSessionHandoff_CreatesFile(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)

	p := config.Paths{
		Home:     home,
		GlobalKB: filepath.Join(home, "global-kb"),
		Memo:     home,
	}
	gmDir := p.GlobalMemoriesDir()
	os.MkdirAll(gmDir, 0o755)

	origFetch := fetchHandoffSignals
	fetchHandoffSignals = func(_ config.Paths, _ string) []coordination.Signal { return nil }
	defer func() { fetchHandoffSignals = origFetch }()

	out := clilog.NewPrefixed("[test]")
	err := generateSessionHandoff(p, out, false, false)
	if err != nil {
		t.Fatalf("generateSessionHandoff() error = %v", err)
	}

	entries, _ := os.ReadDir(gmDir)
	found := false
	for _, e := range entries {
		if strings.HasPrefix(e.Name(), "session-handoff-") {
			found = true
		}
	}
	if !found {
		t.Error("handoff file not created in global-memories dir")
	}
}

