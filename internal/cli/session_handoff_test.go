func TestRenderHandoff_WithSignals(t *testing.T) {
	signals := []coordination.Signal{
		{Type: coordination.SignalActiveState, Machine: "wsl", Message: "Working on fuzz targets for selfhealv4"},
		{Type: coordination.SignalDecision, Machine: "wsl", Message: "Revert to base Qwen3.5-27B"},
		{Type: coordination.SignalTaskDispatch, Machine: "wsl", TargetFor: "macbook", Message: "Review feat/v10-fuzz-evolver-integration", Priority: "high"},
		{Type: coordination.SignalCompleted, Machine: "wsl", Message: "Added 12 fuzz targets across 6 packages"},
	}

	result := renderHandoff("DESKTOP-078M990", "wsl", "2026-03-20", "### global-kb\n\n- Branch: `main`\n\n", signals, handoffRuntimeContext{})

	if !strings.Contains(result, "## Task Summary") {
		t.Error("missing '## Task Summary' section")
	}
	if !strings.Contains(result, "Working on fuzz targets for selfhealv4") {
		t.Error("missing active-state signal content")
	}
	if !strings.Contains(result, "## Decisions Made") {
		t.Error("missing '## Decisions Made' section")
	}
	if !strings.Contains(result, "Revert to base Qwen3.5-27B") {
		t.Error("missing decision signal content")
	}
	if !strings.Contains(result, "## Delegated Tasks") {
		t.Error("missing '## Delegated Tasks' section")
	}
	if !strings.Contains(result, "*(for macbook)*") {
		t.Error("missing target annotation")
	}
	if strings.Contains(result, "<!-- TODO: fill in 1-2 sentences") {
		t.Error("TODO stubs should be replaced when signals are present")
	}
	if !strings.Contains(result, "signal list") {
		t.Error("resume instructions should mention signal list")
	}
}

