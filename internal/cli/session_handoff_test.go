func TestRenderHandoff_WithBlockers(t *testing.T) {
	signals := []coordination.Signal{
		{Type: coordination.SignalBlocker, Machine: "wsl", Message: "GPU OOM prevents large model loading"},
	}

	result := renderHandoff("DESKTOP-078M990", "wsl", "2026-03-20", "", signals)

	if !strings.Contains(result, "**Blocker**: GPU OOM prevents large model loading") {
		t.Error("blocker should appear in Open Items section")
	}
	if strings.Contains(result, "<!-- TODO: numbered list of remaining work -->") {
		t.Error("Open Items TODO should be replaced when blockers are present")
	}
}

