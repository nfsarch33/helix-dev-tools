func TestGenerateSessionHandoff_ForceOverwrites(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)

	p := config.Paths{
		Home:     home,
		GlobalKB: filepath.Join(home, "global-kb"),
		Memo:     home,
	}
	gmDir := p.GlobalMemoriesDir()
	os.MkdirAll(gmDir, 0o755)

	origFetch := fetchHandoffSignals
	fetchHandoffSignals = func(_ config.Paths, _ string) []coordination.Signal {
		return []coordination.Signal{
			{Type: coordination.SignalActiveState, Machine: "wsl", Message: "Updated content"},
		}
	}
	defer func() { fetchHandoffSignals = origFetch }()

	out := clilog.NewPrefixed("[test]")
	_ = generateSessionHandoff(p, out, false, false)
	err := generateSessionHandoff(p, out, true, false)
	if err != nil {
		t.Fatalf("force overwrite error = %v", err)
	}

	entries, _ := os.ReadDir(gmDir)
	for _, e := range entries {
		if strings.HasPrefix(e.Name(), "session-handoff-") {
			data, _ := os.ReadFile(filepath.Join(gmDir, e.Name()))
			if !strings.Contains(string(data), "Updated content") {
				t.Error("force overwrite should contain new signal content")
			}
		}
	}
}
